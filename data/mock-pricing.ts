export type Provider = 'aws' | 'azure' | 'gcp'
export type ServiceType = 'compute' | 'database' | 'storage'
export type RITerm = '1yr' | '3yr'

export interface InstanceSpec {
  id: string
  provider: Provider
  service: ServiceType
  name: string
  specs: string
  /** For compute/database: price per hour. For storage: price per month. */
  price: number
  priceUnit: 'hour' | 'month'
  regions: string[]
  /** Percentage discount with 1-year reserved instance */
  riDiscount1yr: number
  /** Percentage discount with 3-year reserved instance */
  riDiscount3yr: number
}

