/** Hours per month used for cost calculations */
export const HOURS_PER_MONTH = 730

export function getMonthlyOnDemandPrice(
  instance: InstanceSpec,
  utilization: number
): number {
  const baseMonthly =
    instance.priceUnit === 'hour'
      ? instance.price * HOURS_PER_MONTH
      : instance.price
  return baseMonthly * (utilization / 100)
}

export function getMonthlyRIPrice(
  instance: InstanceSpec,
  utilization: number,
  term: RITerm
): number {
  const onDemand = getMonthlyOnDemandPrice(instance, utilization)
  const discount = term === '1yr' ? instance.riDiscount1yr : instance.riDiscount3yr
  return onDemand * (1 - discount / 100)
}
